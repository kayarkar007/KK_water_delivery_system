const express = require('express');
const router = express.Router();
const DailyReport = require('../models/DailyReport');
const Billing = require('../models/Billing');
const Customer = require('../models/Customer');

// ─── GET daily report ─────────────────────────────────
router.get('/daily/:date', async (req, res) => {
  try {
    const report = await DailyReport.findOne({ dateString: req.params.date });
    if (!report) {
      return res.status(404).json({ 
        success: false, 
        message: 'Is date ka koi report nahi hai' 
      });
    }
    res.json({ success: true, data: report });
  } catch (err) {
    res.status(500).json({ success: false, message: err.message });
  }
});

// ─── GET recent daily reports ─────────────────────────
router.get('/daily', async (req, res) => {
  try {
    const limit = parseInt(req.query.limit) || 30;
    const reports = await DailyReport.find()
      .sort({ date: -1 })
      .limit(limit)
      .select('dateString totalTrips totalCustomersServed totalCansDelivered totalNormalCansDelivered totalCoolCansDelivered totalRevenue');
    res.json({ success: true, data: reports });
  } catch (err) {
    res.status(500).json({ success: false, message: err.message });
  }
});

// ─── GET monthly report ───────────────────────────────
router.get('/monthly/:year/:month', async (req, res) => {
  try {
    const { year, month } = req.params;
    const monthKey = `${year}-${String(month).padStart(2, '0')}`;
    
    // Get all daily reports for this month
    const startDate = `${year}-${String(month).padStart(2, '0')}-01`;
    const endMonth = parseInt(month) === 12 ? 1 : parseInt(month) + 1;
    const endYear = parseInt(month) === 12 ? parseInt(year) + 1 : parseInt(year);
    const endDate = `${endYear}-${String(endMonth).padStart(2, '0')}-01`;
    
    const dailyReports = await DailyReport.find({
      dateString: { $gte: startDate, $lt: endDate }
    }).sort({ dateString: 1 });
    
    // Get billing data for this month
    const billingData = await Billing.find({ monthKey }).sort({ customerName: 1 });
    
    // Aggregate totals
    const totals = {
      totalDeliveryDays: dailyReports.length,
      totalTrips: dailyReports.reduce((sum, r) => sum + r.totalTrips, 0),
      totalCustomersServed: dailyReports.reduce((sum, r) => sum + r.totalCustomersServed, 0),
      totalNormalCansDelivered: dailyReports.reduce((sum, r) => sum + r.totalNormalCansDelivered, 0),
      totalCoolCansDelivered: dailyReports.reduce((sum, r) => sum + r.totalCoolCansDelivered, 0),
      totalCansDelivered: dailyReports.reduce((sum, r) => sum + r.totalCansDelivered, 0),
      totalEmptyCansCollected: dailyReports.reduce((sum, r) => sum + r.totalEmptyCansCollected, 0),
      totalBilled: billingData.reduce((sum, b) => sum + b.totalAmount, 0),
      totalPaid: billingData.reduce((sum, b) => sum + b.paidAmount, 0),
      totalPending: billingData.reduce((sum, b) => sum + b.pendingAmount, 0)
    };
    
    res.json({ 
      success: true, 
      data: { totals, dailyReports, billingData } 
    });
  } catch (err) {
    res.status(500).json({ success: false, message: err.message });
  }
});

