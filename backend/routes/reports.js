// ─── GET WhatsApp report text (monthly) ───────────────
router.get('/whatsapp/monthly/:year/:month', async (req, res) => {
  try {
    const { year, month } = req.params;
    const monthKey = `${year}-${String(month).padStart(2, '0')}`;
    const monthNames = ['', 'January', 'February', 'March', 'April', 'May', 'June',
      'July', 'August', 'September', 'October', 'November', 'December'];
    
    const billingData = await Billing.find({ monthKey }).sort({ customerName: 1 });
    
    let text = `🚰 *KK Events & Water Plant*\n`;
    text += `📅 *Monthly Report - ${monthNames[parseInt(month)]} ${year}*\n`;
    text += `━━━━━━━━━━━━━━━━━━\n\n`;
    
    let totalBilled = 0, totalPaid = 0;
    
    billingData.forEach((b, i) => {
      text += `${i + 1}. *${b.customerName}*\n`;
      text += `   Normal: ${b.normalCansDelivered} × ₹${b.normalCanRate} = ₹${b.normalCansAmount}\n`;
      text += `   Cool: ${b.coolCansDelivered} × ₹${b.coolCanRate} = ₹${b.coolCansAmount}\n`;
      text += `   Total: ₹${b.totalAmount} | Paid: ₹${b.paidAmount} | Due: ₹${b.pendingAmount}\n`;
      text += `   Status: ${b.status === 'cleared' ? '✅ PAID' : b.status === 'partial' ? '⚠️ PARTIAL' : '❌ PENDING'}\n\n`;
      totalBilled += b.totalAmount;
      totalPaid += b.paidAmount;
    });
    
    text += `━━━━━━━━━━━━━━━━━━\n`;
    text += `💰 *Total Billed:* ₹${totalBilled}\n`;
    text += `✅ *Total Paid:* ₹${totalPaid}\n`;
    text += `⏳ *Total Pending:* ₹${totalBilled - totalPaid}\n`;
    text += `━━━━━━━━━━━━━━━━━━\n`;
    text += `✅ Generated by KK Delivery App`;
    
    res.json({ success: true, data: { text } });
  } catch (err) {
    res.status(500).json({ success: false, message: err.message });
  }
});

module.exports = router;
